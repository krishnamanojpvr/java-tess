// Boxing is the process of converting a primitive data type to a wrapper class object.
// Unboxing is the process of converting a wrapper class object to a primitive data type.
// Auto-boxing is the process of converting a primitive data type to a wrapper class object automatically.


public class Boxing {
    public static void main(String[] args) {
        int h = 90;
        Integer i  = 30;
        System.out.println(h + i);
        
        String s = "100";  // String is not a wwrapper class object. It is a class. 
        
        System.out.println(s);
    }
}
